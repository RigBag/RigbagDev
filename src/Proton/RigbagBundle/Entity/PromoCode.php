<?php

namespace Proton\RigbagBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Proton\RigbagBundle\Entity\PromoCode
 */
class PromoCode
{
    /**
     * @var integer $id
     */
    private $id;

    /**
     * @var string $code
     */
    private $code;

    /**
     * @var integer $to_remove
     */
    private $to_remove;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set code
     *
     * @param string $code
     * @return PromoCode
     */
    public function setCode($code)
    {
        $this->code = $code;
    
        return $this;
    }

    /**
     * Get code
     *
     * @return string 
     */
    public function getCode()
    {
        return $this->code;
    }

    /**
     * Set to_remove
     *
     * @param integer $toRemove
     * @return PromoCode
     */
    public function setToRemove($toRemove)
    {
        $this->to_remove = $toRemove;
    
        return $this;
    }

    /**
     * Get to_remove
     *
     * @return integer 
     */
    public function getToRemove()
    {
        return $this->to_remove;
    }
}